export const getNotificationIcon = (type: string) => {
  const icons: Record<string, string> = {
    booking_confirmation: 'check-circle',
    departure_reminder: 'clock',
    bus_delay: 'alert-triangle',
    driver_change: 'user',
    vehicle_change: 'bus',
    payment_confirmed: 'credit-card',
    subscription_expired: 'calendar-x',
    complaint_update: 'message-square',
    lost_item_found: 'search',
    admin_message: 'bell',
    emergency_alert: 'alert-octagon',
  };
  return icons[type] || 'bell';
};

export const getPriorityColor = (priority: string) => {
  const colors: Record<string, string> = {
    low: 'bg-gray-100 text-gray-700 border-gray-200',
    medium: 'bg-blue-100 text-blue-700 border-blue-200',
    high: 'bg-orange-100 text-orange-700 border-orange-200',
    urgent: 'bg-red-100 text-red-700 border-red-200',
  };
  return colors[priority] || colors.low;
};
