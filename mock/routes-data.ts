export const getRouteStatusColor = (status: string) => {
  const colors: Record<string, string> = {
    on_time: 'bg-emerald-100 text-emerald-700 border-emerald-200',
    delayed: 'bg-orange-100 text-orange-700 border-orange-200',
    finished: 'bg-gray-100 text-gray-700 border-gray-200',
    cancelled: 'bg-red-100 text-red-700 border-red-200',
  };
  return colors[status] || colors.on_time;
};

export const getRouteStatusLabel = (status: string, language: string) => {
  const labels: Record<string, Record<string, string>> = {
    ar: {
      on_time: 'في الوقت',
      delayed: 'متأخر',
      finished: 'منتهي',
      cancelled: 'ملغي',
    },
    fr: {
      on_time: 'À l\'heure',
      delayed: 'Retard',
      finished: 'Terminé',
      cancelled: 'Annulé',
    },
  };
  return labels[language]?.[status] || status;
};
